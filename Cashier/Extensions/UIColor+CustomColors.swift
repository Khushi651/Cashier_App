import Foundation
import UIKit

extension UIColor {
    struct CustomColors {
        static let Black = UIColor(hex: "#000000")
        static let PrimaryYellow = UIColor(hex: "#fecf67")
        static let SecondaryBlur = UIColor(hex: "#fecf67")
        static let LightSkyBlue = UIColor(hex: "#e8faff")
        static let GreatLightSkyBlue = UIColor(hex: "#f8f8f8")
        
        //Gradient Colors
        static let Gradient1 = UIColor(hex: "#fecd75")
        static let Gradient2 = UIColor(hex: "#ffc6b6")
    }
}
